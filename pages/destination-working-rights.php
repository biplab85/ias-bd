                <h3 class="page-section-title text-uppercase my-30">AFTER STUDY</h3>

                <p>Unlike in some other English-speaking countries, there is no automatic right associated with your student visa to stay on and work for a period of time in Australia after you complete your degree. When you receive your student visa, the government lets you in on basis of temporary entry for study with the understanding that you will leave upon completion of course and before the visa will expire. However, Australia continues to have demand for foreign workers, and students that have earned a degree in Australia have a special work visa category. There are also several other visa categories open to international students, and Australia has a very straightforward path to permanent residency using a points system . If you want to stay and work after you graduate, you must apply and obtain a work visa.</p>

                <h3 class="page-section-title text-uppercase my-30">HERE ARE A FEW OF THE CATEGORIES<br> OF VISA THAT ARE AVAILABLE:</h3>

                <p><b>485 Skilled Graduate (Temporary Visa)</b><br>This visa is the most common option available to international students after graduating. With a 485 visa, you are allowed to stay in Australia for 18 months to gain work experience only if you have just completed at least two years of study in Australia.</p>

                <p><b>402 Training and (Research Visa)</b><br>Another option for international students, the 442 visa is for students to improve their work skills for up to two years while being sponsored by an employer.</p>

                <p><b>487 Regional (Sponsored Visa)</b><br>This visa provides a pathway for permanent residency while allowing you to work for 3 years and work in a specialized regional area</p>

                <p><b>887 Skilled Regional (Residence Visa)</b><br>The 887 visa awards you permanent residency and with this you can live and work in Australia permanently. You must have already lived in Australia for two years, have a year of work experience and have sufficient points.</p>

                <p><b>885 Skilled Independent (Residence Visa)</b><br>The 885 visa allows you to live as a permanent resident and work without needing to be sponsored</p>

                <p><b>886 Skilled Sponsor (Resident Visa)</b><br>The 886 visa allows an overseas student to apply for permanent residency and work while being sponsored by an employer.</p>

                <p>As mentioned above, Australia uses a point system to see if someone qualifies for most visas. The point evaluations will be assessed along with your application to see if you qualify. These are a few of the items that gain you points:</p>

